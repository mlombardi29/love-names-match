export interface BabyName {
  id: string;
  name: string;
  gender: 'boy' | 'girl';
  origins: CulturalOrigin[];
  isCustom?: boolean;
}

export const createNameDatabase = (): BabyName[] => {
  const girlNames = POPULAR_GIRL_NAMES.map((data, index) => ({
    id: `girl-${index}`,
    name: data.name,
    gender: 'girl' as const,
    origins: data.origins,
  }));

  const boyNames = POPULAR_BOY_NAMES.map((data, index) => ({
    id: `boy-${index}`,
    name: data.name,
    gender: 'boy' as const,
    origins: data.origins,
  }));

  return [...girlNames, ...boyNames];
};
